    private void getData() {
        int totalBrg;
        boolean ketemu = false;
        PenjualanDAO dao = new PenjualanDAOImpl();
        arrayPenjualan = dao.getPenjualan();
        getDetail();

        for (int i = 0; i < arrayPenjualan.size(); i++) {
            totalBrg = 0;
            for (int j = 0; j < arrayDetailPenjualan.size(); j++) {
                if (arrayPenjualan.get(i).getKodePenjualan().equalsIgnoreCase(arrayDetailPenjualan.get(j).getKodePenjualan())) {
                    ketemu = true;
                } else {
                    ketemu = false;
                }
                if (ketemu) {
                    totalBrg = totalBrg + 1;
                }
            }
            arrayPenjualan.get(i).setTotalBrg(totalBrg);
        }

        PenjualanTM tm = new PenjualanTM();
        tm.setArrayPenjualan(arrayPenjualan);
        tablePenjualan.setModel(tm);
    }

