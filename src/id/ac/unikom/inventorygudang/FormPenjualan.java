    private void btnGrafikPenjualanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrafikPenjualanActionPerformed
        DefaultCategoryDataset dcd = new DefaultCategoryDataset();
        boolean sama = false;
        for (int i = 0; i < arrayPenjualan.size(); i++) {
            int jml = 0;
            Penjualan penjualan = new Penjualan();
            penjualan = arrayPenjualan.get(i);
            for (int k = 0; k < arrayPenjualan.size(); k++) {
                Penjualan penjualan2 = new Penjualan();
                penjualan2 = arrayPenjualan.get(k);
                String tgl1 = String.valueOf(penjualan.getTglJual());
                String tgl2 = String.valueOf(penjualan2.getTglJual());
                if (tgl1.equals(tgl2)) {
                    sama = true;
                }

                if (sama) {
                    jml = jml + penjualan2.getTotalBrg();
                    sama = false;
                }
            }
            String tanggal = String.valueOf(penjualan.getTglJual());
            dcd.setValue(jml, "Barang", tanggal);
        }
        JFreeChart freeChart = ChartFactory.createBarChart("Grafik Penjualan", "Tanggal", "Jumlah Barang", dcd, PlotOrientation.HORIZONTAL, true, true, true); //String arg0,String arg1,String arg2,Category Datasheet,Plot Orientation,boolean arg4,boolean arg5,boolean arg6
        ChartFrame cf = new ChartFrame("Data Penjualan PUSDAV", freeChart);

        cf.setSize(1000, 500);
        cf.setVisible(true);
        cf.setLocationRelativeTo(null);
    }//GEN-LAST:event_btnGrafikPenjualanActionPerformed

