    private void btnHapusSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusSupplierActionPerformed
        int baris = tableSupplier.getSelectedRow();
        if (baris >= 0) {
            Supplier supplierTerpilih = arraySupplier.get(baris);

            int aksiUser = JOptionPane.showConfirmDialog(this, "Apakah Anda "
                    + "yakin akan menghapus data "
                    + supplierTerpilih.getNama(), "Hapus supplier ?",
                    JOptionPane.YES_NO_OPTION);
            if (aksiUser == JOptionPane.YES_NO_OPTION) {
                String kode = supplierTerpilih.getKodeSupplier();
                SupplierDAO dao = new SupplierDAOImpl();
                boolean sukses;
                sukses = dao.hapusSupplier(kode);

                if (sukses) {
                    JOptionPane.showMessageDialog(this, "Supplier berhasil dihapus");
                } else {
                    JOptionPane.showMessageDialog(this, "Supplier gagal dihapus");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih supplier yang akan dihapus");
        }
        getData(null);
    }//GEN-LAST:event_btnHapusSupplierActionPerformed

