    private void tampilReport() {
        SimpleDateFormat fmt = new SimpleDateFormat("dd/MM/yyyy");
        Date date = new Date();
        String tanggalHariIni = fmt.format(date);
        try {
            File report = new File("src\\id\\ac\\unikom\\inventorygudang\\report\\ReportSupplier.jrxml");
            jasDes = JRXmlLoader.load(report);
            String image = "src\\id\\ac\\unikom\\inventorygudang\\image\\logo-pusdav.png";
            param.put("logo", image);
            param.put("tanggalHariIni", tanggalHariIni);
            jasRep = JasperCompileManager.compileReport(jasDes);
            jasPri = JasperFillManager.fillReport(jasRep, param, koneksi.getConnection());
            JasperViewer.viewReport(jasPri, false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

