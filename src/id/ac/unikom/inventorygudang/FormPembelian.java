    private void getData() {
        PembelianDAO dao = new PembelianDAOImpl();
        arrayPembelian = dao.getPembelian();
    

        PembelianTM tm = new PembelianTM();
        tm.setArrayPembelian(arrayPembelian);
        tablePembelian.setModel(tm);
    }

    private void getData(String cari) {
        PembelianDAO dao = new PembelianDAOImpl();
        arrayPembelian = dao.getPembelian(cari);
        getDetail();

        PembelianTM tm = new PembelianTM();
        tm.setArrayPembelian(arrayPembelian);
        tablePembelian.setModel(tm);
    }
    
    private void getDetail(){
        DetailPembelianDAO dao2 = new DetailPembelianDAOImpl();
        arrayDetailPembelian = dao2.getDetailPembelian();
    }

    private void refreshData() {
        getData();
    }

