    private void btnDetailPembelianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailPembelianActionPerformed
        int baris = tablePembelian.getSelectedRow();
        if (baris >= 0) {
            Pembelian dataTerpilih = arrayPembelian.get(baris);
            System.out.println(""+arrayPembelian.get(baris).getKodePembelian());
            FormDetailPembelian fdpm = new FormDetailPembelian(this, true, dataTerpilih, hakAkses, namaUser,idAdmin);
            fdpm.setLocationRelativeTo(null);
            fdpm.setVisible(true);
            
        } else {
            JOptionPane.showMessageDialog(rootPane, "Pilih data pada tabel"
                    + " pembelian");
        }
    }//GEN-LAST:event_btnDetailPembelianActionPerformed

