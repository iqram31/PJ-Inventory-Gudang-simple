   private void getData() {
        SupplierDAO dao = new SupplierDAOImpl();
        arraySupplier = dao.getSupplier();

        SupplierTM tm = new SupplierTM();
        tm.setArraySupplier(arraySupplier);
        tableDaftarSupplier.setModel(tm);
    }

    private void getData(String nama) {
        SupplierDAO dao = new SupplierDAOImpl();
        arraySupplier = dao.getSupplier(nama);

        SupplierTM tm = new SupplierTM();
        tm.setArraySupplier(arraySupplier);
        tableDaftarSupplier.setModel(tm);
    }

