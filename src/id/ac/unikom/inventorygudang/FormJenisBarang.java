    private void btnUbahJenisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahJenisActionPerformed
        int baris = tableJenisBarang.getSelectedRow();
        if (baris >= 0) {
            JenisBarang jenisBarangTerpilih = arrayJenisBarang.get(baris);
            FormUbahJenis fuj = new FormUbahJenis(this, true, jenisBarangTerpilih);
            fuj.setLocationRelativeTo(null);
            fuj.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih jenis barang yang akan diubah");
        }
        getData();
    }//GEN-LAST:event_btnUbahJenisActionPerformed

