    private void btnHapusJenisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusJenisActionPerformed
        int baris = tableJenisBarang.getSelectedRow();
        //Validasi barang yang terpilih
        if (baris >= 0) {
            JenisBarang jenisBarangTerpilih = arrayJenisBarang.get(baris);

            int aksiUser = JOptionPane.showConfirmDialog(this, "Apakah Anda "
                    + "yakin akan menghapus data "
                    + jenisBarangTerpilih.getNamaJenisBarang(), "Hapus Barang ?",
                    JOptionPane.YES_NO_OPTION);
            if (aksiUser == JOptionPane.YES_NO_OPTION) {
                int kode = jenisBarangTerpilih.getNoJenis();
                JenisBarangDAO dao = new JenisBarangDAOImpl();
                boolean sukses;
                sukses = dao.hapusJenisBarang(kode);

                if (sukses) {
                    JOptionPane.showMessageDialog(this, "Jenis barang berhasil dihapus");
                } else {
                    JOptionPane.showMessageDialog(this, "Jenis bbarang gagal dihapus");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih jenis barang yang akan dihapus");
        }
        getData(null);
    }//GEN-LAST:event_btnHapusJenisActionPerformed

