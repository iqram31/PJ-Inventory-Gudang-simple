    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        int baris = tableDetailPembelian.getSelectedRow();
        if (baris >= 0) {
            DetailPembelian dataTerpilih = arrayDetail.get(baris);
            FormUbahPesanan fup = new FormUbahPesanan(null, true, dataTerpilih);
            fup.setLocationRelativeTo(null);
            fup.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Pilih data pada tabel"
                    + " pemesanan");
        }
        
    }//GEN-LAST:event_btnTambahActionPerformed

