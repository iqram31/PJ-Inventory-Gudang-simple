    public FormDetailPembelian(java.awt.Frame parent, boolean modal, Pembelian pembelian, String akses, String nama, int noAdmin) {
        super(parent, modal);
        initComponents();
        namaUser = nama;
        hakAkses = akses;
        idAdmin = noAdmin;
        if (hakAkses.equalsIgnoreCase("Manajer")) {
            btnTambah.setVisible(false);
        }
        getData();
        String tanggalPembelian = String.valueOf(pembelian.getTglBeli());
        labelKodePembelian.setText(pembelian.getKodePembelian());
        labelTanggalPembelian.setText(tanggalPembelian);

        boolean ketemu = false;
        for (int i = 0; i < arrayDetailPembelian.size(); i++) {
            if (pembelian.getKodePembelian().equalsIgnoreCase(arrayDetailPembelian.get(i).getKodePembelian())) {
                ketemu = true;
            } else {
                ketemu = false;
            }
            if (ketemu) {
                DetailPembelian dataDetail = new DetailPembelian();
                dataDetail.setKodeBarang(arrayDetailPembelian.get(i).getKodeBarang());
                dataDetail.setKodePembelian(arrayDetailPembelian.get(i).getKodePembelian());
                dataDetail.setNamaBarang(arrayDetailPembelian.get(i).getNamaBarang());
                dataDetail.setJenisBarang(arrayDetailPembelian.get(i).getJenisBarang());
                dataDetail.setDetailJenisBarang(arrayDetailPembelian.get(i).getDetailJenisBarang());
                dataDetail.setJumlahBarang(arrayDetailPembelian.get(i).getJumlahBarang());
                arrayDetail.add(dataDetail);
            }
        }
        DetailPembelianTM dp = new DetailPembelianTM();
        dp.setArrayDetailPembelian(arrayDetail);
        tableDetailPembelian.setModel(dp);
    }

